"""
Week 2 线性代数 — 动手实战 9.1：语义相似度

用最简单的词袋模型模拟向量化，计算余弦相似度。
直观感受："向量之间的夹角 = 内容相似度"。

运行依赖：numpy
"""

import numpy as np

# ============================================================
# 0. 词汇表 + 文档向量（词袋模型）
# ============================================================
# 词汇表：[机器学习, 深度学习, 神经网络, 梯度, 烹饪, 菜谱, 厨房, 美食]
VOCAB = ["机器学习", "深度学习", "神经网络", "梯度", "烹饪", "菜谱", "厨房", "美食"]

s1 = np.array([3, 2, 1, 1, 0, 0, 0, 0])  # AI文章1
s2 = np.array([2, 3, 2, 0, 0, 0, 0, 0])  # AI文章2
s3 = np.array([0, 0, 0, 0, 2, 3, 1, 1])  # 烹饪文章
s4 = np.array([1, 1, 0, 1, 0, 0, 0, 1])  # 混合内容


# ============================================================
# 1. 距离/相似度计算函数
# ============================================================
def dot(v1: np.ndarray, v2: np.ndarray) -> float:
    return float(np.sum(v1 * v2))


def l2_norm(v: np.ndarray) -> float:
    return float(np.sqrt(dot(v, v)))


def cosine(v1: np.ndarray, v2: np.ndarray) -> float:
    return dot(v1, v2) / (l2_norm(v1) * l2_norm(v2))


def euclidean(v1: np.ndarray, v2: np.ndarray) -> float:
    return float(np.sqrt(np.sum((v1 - v2) ** 2)))


# ============================================================
# 2. 对比实验
# ============================================================
print("=" * 55)
print("语义相似度实验：AI文章 vs 烹饪文章")
print("=" * 55)

pairs = [
    ("s1 vs s2 (两篇AI)",  s1, s2),
    ("s1 vs s3 (AI vs 烹饪)", s1, s3),
    ("s1 vs s4 (AI vs 混合)", s1, s4),
    ("s3 vs s4 (烹饪 vs 混合)", s3, s4),
]

for label, a, b in pairs:
    print(f"\n{label}:")
    print(f"  余弦相似度 = {cosine(a, b):.4f}")
    print(f"  欧氏距离   = {euclidean(a, b):.4f}")
    print(f"  点积       = {dot(a, b):.4f}")

print("\n" + "=" * 55)
print("结论：余弦越接近 1 → 内容越像（方向一致）")
print("      余弦越接近 0 → 内容无关（方向正交）")
print("=" * 55)


# ============================================================
# 3. 补充：为什么余弦比点积更有意义？
# ============================================================
print("\n--- 点积 vs 余弦相似度 ---")
long_doc  = np.array([5, 5, 5, 5, 0, 0, 0, 0])   # 长AI文章
short_doc = np.array([1, 1, 1, 1, 0, 0, 0, 0])   # 短AI文章（同主题）

print(f"长文章点积(与s1)   = {dot(s1, long_doc):.4f}")
print(f"短文章点积(与s1)   = {dot(s1, short_doc):.4f}")
print(f"  ↑ 长文档天然占优，不代表更相关")

print(f"长文章余弦(与s1)   = {cosine(s1, long_doc):.4f}")
print(f"短文章余弦(与s1)   = {cosine(s1, short_doc):.4f}")
print(f"  ↑ 余弦消除了长度偏差，只看方向（语义）是否一致")
